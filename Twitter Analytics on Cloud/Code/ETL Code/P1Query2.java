		public int CalculateSentimentScore(String tweet) {
			int sentimentScore = 0;
			String[] parts = tweet.split("[^0-9A-Za-z]");
			for (String each : parts) {
				String word = each.toLowerCase();
				if (sentiScore.containsKey(word)) {
					sentimentScore += sentiScore.get(word);
				}
			}
			return sentimentScore;
		}

